import type { Metadata } from "next";
import { Geist, Geist_Mono, Source_Serif_4 } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

// Used only for document preview headings (see DocumentPreview.tsx) - the
// serif marks a document as a document, distinct from the sans-serif app
// chrome around it.
const sourceSerif = Source_Serif_4({
  variable: "--font-document-serif",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Prelegal",
  description: "Draft legal agreements with an AI assistant",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html
      lang="en"
      className={`${geistSans.variable} ${geistMono.variable} ${sourceSerif.variable} h-full antialiased`}
    >
      <body className="min-h-full flex flex-col" suppressHydrationWarning>
        {children}
      </body>
    </html>
  );
}
